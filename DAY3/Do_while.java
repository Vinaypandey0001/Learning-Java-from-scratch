// Program to show how do-while loop works 

public class Do_while {
    public static void main(String[] args) {

    int i=0; //here we initialize the value of i
    do{
        System.out.println(i);
        i++; // here we increment  the value of i by 1
    }
    while(i<5); // At last condition will checks  after the initialization and updation.
}
}
