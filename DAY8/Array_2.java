import java.io.*;

class Array_2 {
    public static void main(String[] args)
    {
        // Syntax
        int[][] arr = new int[3][3];
        // 3 row and 3 column

          // Number of Rows
        System.out.println("Number of Rows:"+
                           arr.length);
      
          // Number of Columns
        System.out.println("Number of Columns:"+
                           arr[0].length);
    }
}
